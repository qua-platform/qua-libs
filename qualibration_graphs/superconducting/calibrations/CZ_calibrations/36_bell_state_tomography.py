description = """
**BELL STATE TOMOGRAPHY EXPERIMENT**

This experiment performs full quantum state tomography of a two-qubit Bell state in order to
characterize the fidelity and properties of the entangled state generated by an entangling gate
(e.g. CZ).

The sequence prepares a Bell state, applies a complete set of tomography rotations on both qubits,
and measures the resulting expectation values to reconstruct the two-qubit density matrix.

For each tomography setting, we measure:

1. The joint measurement outcomes of the two qubits in the computational basis.

**State tomography:**
To fully reconstruct the two-qubit quantum state, a set of single-qubit pre-measurement rotations
is applied. These rotations map different Pauli operator combinations (I, X, Y, Z) onto the
measurement basis. By repeating the experiment for all required rotation combinations, the full
density matrix of the Bell state can be reconstructed.

The tomography process involves:

1. Preparing both qubits in the ground state |g⟩
2. Creating the Bell state using single-qubit rotations and an entangling gate (e.g. CZ)
3. Applying a complete set of tomography rotations on both qubits
4. Measuring both qubits in the computational basis
5. Reconstructing the two-qubit density matrix from the measured expectation values

The quality of the Bell state is evaluated by extracting quantities such as:

1. State fidelity with respect to the target Bell state
2. Population and coherence terms of the density matrix
3. Entanglement metrics (e.g. concurrence or Bell-state parity)

Prerequisites:
- Calibrated single-qubit gates and readout for both qubits in the pair
- A calibrated entangling macro (``operation``) on each qubit pair
- Two-qubit readout confusion matrix from node 35_two_qubit_confusion_matrix

State update:
- Bell-state fidelity and purity on ``macros[operation].fidelity["Bell_State"]``
  for each successfully analysed pair
"""

